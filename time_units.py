"""Utility conversion factors from picoseconds to other units."""

# Conversion factors for picosecond timestamps
TO_NS = 1e-3  # picoseconds to nanoseconds
TO_US = 1e-6  # picoseconds to microseconds
TO_MS = 1e-9  # picoseconds to milliseconds
TO_S = 1e-12  # picoseconds to seconds
TO_MIN = TO_S / 60  # picoseconds to minutes

